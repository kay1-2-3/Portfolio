export const Button = ({className = "", size ="default", children}) => {
    const baseClasses = "relative overflow-hidden rounded-full font-medium focus:outline-none focus:ring-2 focus:ring-primary bg-primary text-primary-foreground hover:bg-primary/90 shadow-primary/25";

    const sizeClasses = {
        sm: "text-sm px-4 py-2",
        default: "text-base px-6 py-3",
        lg: "text-lg px-8 py-4",
    }

    const classes = `${baseClasses} ${sizeClasses[size]} ${className}`;
    return (
    <button className={classes}>
        <span className="relative flex items-center justify-center gap-2">
            {children}
        </span>
    </button>
    );
};
